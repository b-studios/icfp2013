testEval :: P -> [Word64] -> [Word64] -> Test
testEval p inputs outputs = testGroup (show p)
  [ testCase (showHex input . showString " -> " . showHex output $ "") $
      assertEqual "" (evalP input p) output
  | input <- inputs
  | output <- outputs
  ]

